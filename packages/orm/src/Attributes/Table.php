declare(strict_types=1);

namespace Windwalker\ORM\Attributes;

/**
 * The Table class.
 */
#[\Attribute]
class Table
{
    protected string $name;

    /**
     * Table constructor.
     *
     * @param  string  $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
